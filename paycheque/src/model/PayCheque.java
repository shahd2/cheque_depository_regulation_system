package model;

public class PayCheque {
	
	private String acc_from;
	private String acc_to;
	private String amount;
	private String image;
	private String cheque_id;
	private String image_name; 
	private String qr;
	public String getAcc_from() {
		return acc_from;
	}
	public void setAcc_from(String acc_from) {
		this.acc_from = acc_from;
	}
	public String getAcc_to() {
		return acc_to;
	}
	public void setAcc_to(String acc_to) {
		this.acc_to = acc_to;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getCheque_id() {
		return cheque_id;
	}
	public void setCheque_id(String cheque_id) {
		this.cheque_id = cheque_id;
	}
	public String getImage_name() {
		return image_name;
	}
	public void setImage_name(String image_name) {
		this.image_name = image_name;
	}
	public String getQr() {
		return qr;
	}
	public void setQr(String qr) {
		this.qr = qr;
	}

	
	
}
